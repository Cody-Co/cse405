const os = require('os');

// Get platform
console.log(os.platform());

// CPU ARCH
console.log(os.arch());
// CPU Core info
console.log(os.cpus());

// Free memory
console.log(os.freemem());
// Total memory
console.log(os.totalmem());

// Home console.dir(
console.log(os.homedir());

// UPtime

console.log(os.uptime());
